/**
 * Enum representing the status of a department.
 */
public enum DepartmentStatusEnum {
    /**
     * Department is active and operational.
     */
    ACTIVE,

    /**
     * Department is inactive and not currently operational.
     */
    INACTIVE,

    /**
     * Department is archived and no longer in use.
     */
    ARCHIVED
}
