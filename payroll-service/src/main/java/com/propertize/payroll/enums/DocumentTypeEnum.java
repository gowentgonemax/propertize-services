package com.propertize.payroll.enums;

/**
 * Enum representing document types for employee documents.
 */
public enum DocumentTypeEnum {
    /**
     * W-4 tax withholding form.
     */
    W4,

    /**
     * W-2 wage and tax statement.
     */
    W2,

    /**
     * I-9 employment eligibility verification.
     */
    I9,

    /**
     * Employment offer letter.
     */
    OFFER_LETTER,

    /**
     * Employment contract.
     */
    EMPLOYMENT_CONTRACT,

    /**
     * Non-disclosure agreement.
     */
    NDA,

    /**
     * Government-issued ID.
     */
    ID_PROOF,

    /**
     * Passport.
     */
    PASSPORT,

    /**
     * Work visa.
     */
    VISA,

    /**
     * Work permit.
     */
    WORK_PERMIT,

    /**
     * Background check report.
     */
    BACKGROUND_CHECK,

    /**
     * Drug test results.
     */
    DRUG_TEST,

    /**
     * Direct deposit authorization form.
     */
    DIRECT_DEPOSIT_FORM,

    /**
     * Benefits enrollment form.
     */
    BENEFITS_ENROLLMENT,

    /**
     * Performance review document.
     */
    PERFORMANCE_REVIEW,

    /**
     * Termination letter.
     */
    TERMINATION_LETTER,

    /**
     * Social Security card.
     */
    SSN_CARD,

    /**
     * Birth certificate.
     */
    BIRTH_CERTIFICATE,

    /**
     * Other document type.
     */
    OTHER
}
