package com.propertize.payroll.enums;

/**
 * Enum representing export file types for payroll.
 */
public enum ExportTypeEnum {
    /**
     * QuickBooks IIF format.
     */
    QUICKBOOKS_IIF,

    /**
     * QuickBooks QBO format.
     */
    QUICKBOOKS_QBO,

    /**
     * Comma-separated values file.
     */
    CSV,

    /**
     * Microsoft Excel format.
     */
    EXCEL,

    /**
     * ACH NACHA file format for direct deposits.
     */
    ACH_NACHA,

    /**
     * JSON format.
     */
    JSON,

    /**
     * XML format.
     */
    XML,

    /**
     * PDF format.
     */
    PDF,

    /**
     * Sage accounting format.
     */
    SAGE,

    /**
     * Xero accounting format.
     */
    XERO
}
